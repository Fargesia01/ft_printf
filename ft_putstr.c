#include "ft_printf.h"

int	ft_putstr(char *str, int c)
{
	int	x;

	x = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		return (c + 6);
	}
	while (str[x])
	{
		write(1, &str[x], 1);
		x++;
	}
	return (c + x);
}
